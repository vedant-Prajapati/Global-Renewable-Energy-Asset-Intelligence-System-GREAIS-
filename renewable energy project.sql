-- 9. Calculate yearly revenue growth percentage.
WITH yearly_revenue AS (
    SELECT
        YEAR(sale_date) AS sales_year,
        SUM(revenue_usd) AS total_revenue
    FROM energy_sales
    GROUP BY YEAR(sale_date)
)
SELECT
    sales_year,
    ROUND(total_revenue, 2) AS total_revenue_usd,
    ROUND(
        (
            (total_revenue -
             LAG(total_revenue) OVER (ORDER BY sales_year))
            /
            LAG(total_revenue) OVER (ORDER BY sales_year)
        ) * 100,
        2
    ) AS revenue_growth_percentage
FROM yearly_revenue
ORDER BY sales_year;


-- 10. Find plants having more than 3 grid failures in a month.
SELECT
    pm.plant_id,
    pm.plant_name,
    YEAR(gf.failure_date) AS year,
    MONTH(gf.failure_date) AS month,
    COUNT(gf.failure_id) AS total_failures
FROM plant_master pm
JOIN grid_failures gf
    ON pm.plant_id = gf.plant_id
GROUP BY
    pm.plant_id,
    pm.plant_name,
    YEAR(gf.failure_date),
    MONTH(gf.failure_date)
HAVING COUNT(gf.failure_id) > 3
ORDER BY total_failures DESC;

-- 11. Calculate average temperature impact on solar energy generation.
SELECT
    CASE
        WHEN wd.temperature < 20 THEN 'Below 20°C'
        WHEN wd.temperature BETWEEN 20 AND 30 THEN '20-30°C'
        WHEN wd.temperature BETWEEN 31 AND 40 THEN '31-40°C'
        ELSE 'Above 40°C'
    END AS temperature_range,
    ROUND(AVG(eg.energy_generated_mwh), 2) AS avg_energy_generated_mwh
FROM plant_master pm
JOIN energy_generation eg
    ON pm.plant_id = eg.plant_id
JOIN weather_data wd
    ON pm.country = wd.country
    AND DATE(eg.datetime) = DATE(wd.timestamp)
WHERE pm.plant_type = 'Solar'
GROUP BY temperature_range
ORDER BY temperature_range;

-- 12. Find cumulative revenue generated by each plant.
SELECT
    pm.plant_id,
    pm.plant_name,
    es.sale_date,
    es.revenue_usd,
    ROUND(
        SUM(es.revenue_usd) OVER (
            PARTITION BY pm.plant_id
            ORDER BY es.sale_date
            ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
        ),
        2
    ) AS cumulative_revenue_usd
FROM plant_master pm
JOIN energy_sales es
    ON pm.plant_id = es.plant_id
ORDER BY
    pm.plant_id,
    es.sale_date;
    
