    pm.plant_name,
    sr.timestamp,
    sr.temperature,
    sr.vibration_level,
    sr.voltage,
    sr.humidity
FROM sensor_readings sr
JOIN plant_master pm
    ON sr.plant_id = pm.plant_id
WHERE sr.temperature > 80
   OR sr.vibration_level > 8
   OR sr.voltage < 200
   OR sr.humidity > 90
ORDER BY sr.timestamp DESC;

-- 19. Create rolling 7-day average energy production trend.
WITH daily_generation AS (
    SELECT
        DATE(datetime) AS generation_date,
        SUM(energy_generated_mwh) AS daily_energy_generated
    FROM energy_generation
    GROUP BY DATE(datetime)
)
SELECT
    generation_date,
    daily_energy_generated,
    ROUND(
        AVG(daily_energy_generated) OVER (
            ORDER BY generation_date
            ROWS BETWEEN 6 PRECEDING AND CURRENT ROW
        ),
        2
    ) AS rolling_7_day_avg_mwh
FROM daily_generation
ORDER BY generation_date;

