ORDER BY days_above_95 DESC;

--  16. Rank plants within each country based on profitability.
WITH sales_revenue AS (
    SELECT
        plant_id,
        SUM(revenue_usd) AS sales_revenue
    FROM energy_sales
    GROUP BY plant_id
),
carbon_revenue AS (
    SELECT
        plant_id,
        SUM(credits_generated * market_price_usd) AS carbon_revenue
    FROM carbon_credits
    GROUP BY plant_id
),
maintenance_cost AS (
    SELECT
        plant_id,
        SUM(maintenance_cost) AS maintenance_cost
    FROM maintenance_logs
    GROUP BY plant_id
),
failure_loss AS (
    SELECT
        plant_id,
        SUM(financial_loss_usd) AS failure_loss
    FROM grid_failures
    GROUP BY plant_id
)
SELECT
    pm.country,
    pm.plant_id,
    pm.plant_name,

    ROUND(
        COALESCE(sr.sales_revenue,0)
        + COALESCE(cr.carbon_revenue,0)
        - COALESCE(mc.maintenance_cost,0)
        - COALESCE(fl.failure_loss,0),
        2
    ) AS net_profit_usd,

    DENSE_RANK() OVER (
        PARTITION BY pm.country
        ORDER BY
        (
            COALESCE(sr.sales_revenue,0)
            + COALESCE(cr.carbon_revenue,0)
            - COALESCE(mc.maintenance_cost,0)
            - COALESCE(fl.failure_loss,0)
        ) DESC
    ) AS profitability_rank

FROM plant_master pm
LEFT JOIN sales_revenue sr
    ON pm.plant_id = sr.plant_id
LEFT JOIN carbon_revenue cr
    ON pm.plant_id = cr.plant_id
LEFT JOIN maintenance_cost mc
    ON pm.plant_id = mc.plant_id
LEFT JOIN failure_loss fl
    ON pm.plant_id = fl.plant_id

ORDER BY
    pm.country,
    profitability_rank;
    
