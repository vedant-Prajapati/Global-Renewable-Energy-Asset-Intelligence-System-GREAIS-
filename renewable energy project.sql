-- 1. Find total energy generated by each plant.
SELECT 
    pm.country,
    pm.plant_type,
    pm.plant_name,
    ROUND(SUM(eg.energy_generated_mwh), 2) AS total_energy_generated_mwh
FROM plant_master pm
JOIN energy_generation eg
    ON pm.plant_id = eg.plant_id
GROUP BY 
    pm.country,
    pm.plant_type,
    pm.plant_name
ORDER BY total_energy_generated_mwh DESC;

-- 2. Find top 10 plants with highest efficiency.
SELECT 
    pm.plant_id,
    pm.plant_name,
    pm.plant_type,
    pm.country,
    ROUND(AVG(eg.efficiency_percent), 2) AS avg_efficiency_percent
FROM plant_master pm
JOIN energy_generation eg
    ON pm.plant_id = eg.plant_id
GROUP BY 
    pm.plant_id,
    pm.plant_name,
    pm.plant_type,
    pm.country
ORDER BY avg_efficiency_percent DESC
LIMIT 10;

-- 3. Calculate total revenue generated country-wise.
SELECT 
    pm.country,
    ROUND(SUM(es.revenue_usd), 2) AS total_revenue_usd
FROM plant_master pm
JOIN energy_sales es
    ON pm.plant_id = es.plant_id
GROUP BY pm.country
ORDER BY total_revenue_usd DESC;

-- 4. Find plants with downtime greater than 100 hours.
SELECT 
    pm.plant_id,
    pm.plant_name,
    pm.country,
    ROUND(SUM(eg.downtime_minutes) / 60, 2) AS total_downtime_hours
FROM plant_master pm
JOIN energy_generation eg
    ON pm.plant_id = eg.plant_id
GROUP BY 
    pm.plant_id,
    pm.plant_name,
    pm.country
HAVING SUM(eg.downtime_minutes) / 60 > 100
ORDER BY total_downtime_hours DESC;

